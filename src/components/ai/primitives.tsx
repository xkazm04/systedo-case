/** A past generation as the history strip needs it — structurally satisfied by
 *  useAiTool's AiHistoryEntry, so panels pass `history={history}` straight through. */
export interface ResultHistoryItem {
  savedAt: number;
  payload: { meta: AiMeta };
}

/** Result header: model badge, demo / live status, an optional relative
 *  "generated X ago" stamp for stored reports, and an optional "copy all".
 *  When a panel passes its generation history (2+ entries), a compact
 *  "Předchozí generace" strip renders below — timestamped chips that restore a
 *  past result without a new model call. */
export function ResultMeta({
  meta,
  copyAllText,
  createdAt,
  extra,
  history,
  activeIndex,
  onRestore,
}: {
  meta: AiMeta;
  copyAllText?: string;
  createdAt?: string;
  /** extra pill(s) rendered alongside the model badge (e.g. the analysis period) */
  extra?: ReactNode;
  history?: ResultHistoryItem[];
  activeIndex?: number;
  onRestore?: (index: number) => void;
}) {
  const t = useT(T);
  const fmt = useFormatters();
  const strip =
    history && onRestore && history.length > 1 ? (
      <div className="flex flex-wrap items-center gap-1.5" data-testid="ai-history">
        <span className="text-xs text-muted">{t("historyLabel")}</span>
        {history.map((h, i) => {
          const active = i === (activeIndex ?? 0);
          const when = h.savedAt ? fmt.fmtDateTime(new Date(h.savedAt).toISOString()) : "";
          const label = h.savedAt
            ? fmt.fmtRelative(new Date(h.savedAt).toISOString())
            : h.payload.meta.model;
          return (
            <button
              key={`${h.savedAt}-${i}`}
              type="button"
              onClick={() => onRestore(i)}
              disabled={active}
              title={t("historyRestoreTitle", { when: when || h.payload.meta.model })}
              className={`rounded-pill border px-2.5 py-1 text-xs font-medium transition-colors ${
                active
                  ? "border-brand-300 bg-brand-50 text-brand-800"
                  : "border-line text-muted hover:border-brand-300 hover:text-brand-accent"
              }`}
            >
              {label}
              {h.payload.meta.demo ? ` · ${t("historyDemoTag")}` : ""}
              {active ? ` · ${t("historyCurrent")}` : ""}
            </button>
          );
        })}
      </div>
    ) : null;
  const metaRow = (
    <div className="flex flex-wrap items-center justify-between gap-3">
      <div className="flex flex-wrap items-center gap-2">
        <span className="pill bg-navy-50 text-navy-700">{meta.model}</span>
        {extra}
        {meta.demo ? (
          <span className="pill bg-coral-soft text-coral-600">
            <Info width={13} height={13} />
            {t("demoMode")}
          </span>
        ) : (
          <span className="pill bg-positive-soft text-positive">
            <Check width={13} height={13} />
            {t("generatedBy", { s: (meta.tookMs / 1000).toFixed(1) })}
          </span>
        )}
        {createdAt && (
          <span className="pill bg-navy-50 text-muted" title={fmt.fmtDateTime(createdAt)}>
            <Clock width={13} height={13} />
            <time dateTime={createdAt}>{fmt.fmtRelative(createdAt)}</time>
          </span>
        )}
        {/* token usage + cost — makes the "subscription (free) vs metered API"
            story explicit; only on real provider runs. */}
        {!meta.demo && meta.estCostUsd !== undefined && (
          meta.usage ? (
            <span
              className="pill bg-navy-50 text-muted"
              title={`${fmt.fmtInt(meta.usage.inputTokens)} ${t("inputTokens")} + ${fmt.fmtInt(
                meta.usage.outputTokens
              )} ${t("outputTokens")} ${t("tokensUnit")}`}
            >
              {fmt.fmtInt(meta.usage.totalTokens)} {t("tokensTok")} · ~${meta.estCostUsd.toFixed(4)}
            </span>
          ) : meta.estCostUsd === 0 ? (
            <span
              className="pill bg-navy-50 text-muted"
              title={t("subscriptionFree")}
            >
              {t("subscriptionLabel")}
            </span>
          ) : null
        )}
        {/* the output was auto-corrected to the platform limits */}
        {meta.repaired && (
          <span
            className="pill bg-brand-50 text-brand-700"
            title={
              meta.violations?.length
                ? t("autoRepairedTitleBase", { violations: meta.violations.join("; ") })
                : t("autoRepairedTitleDefault")
            }
          >
            <Bolt width={13} height={13} />
            {t("autoRepaired")}
          </span>
        )}
      </div>
      {copyAllText && <CopyButton text={copyAllText} label={t("copyAll")} />}
    </div>
  );
  if (!strip) return metaRow;
  return (
    <div className="space-y-2.5">
      {metaRow}
      {strip}
    </div>
  );
}

/** Iteration loop under a generated result: a free-text steering note („kratší",
 *  „více na benefity") re-runs the SAME input with the note appended, so the user
 *  can iterate instead of mangling the form — and the changed input naturally
 *  busts the server's response cache. Render only for tools whose server accepts
 *  a `refine` note (the non-gate-locked modes) and only when the hook reports
 *  `canRefine` (a result restored from storage has no payload to re-send). */
export function RefineBar({ onRefine, disabled }: { onRefine: (note: string) => void; disabled?: boolean }) {
  const t = useT(T);
  const [note, setNote] = useState("");
  const canGo = !disabled && note.trim().length >= 2;
  return (
    <form
      data-testid="ai-refine"
      className="rounded-card border border-line bg-surface p-4"
      onSubmit={(e) => {
        e.preventDefault();
        if (canGo) onRefine(note);
      }}
    >
      <p className="text-sm font-medium text-navy-700">{t("refineLabel")}</p>
      <div className="mt-2 flex flex-col gap-2 sm:flex-row">
        <input
          type="text"
          value={note}
          onChange={(e) => setNote(e.target.value)}
          maxLength={500}
          placeholder={t("refinePlaceholder")}
          aria-label={t("refineLabel")}
          className={inputClass}
        />
        <button
          type="submit"
          disabled={!canGo}
          className="inline-flex shrink-0 items-center justify-center gap-1.5 rounded-pill border border-line px-4 py-2 text-sm font-medium text-navy-700 transition-colors hover:border-brand-300 hover:text-brand-accent disabled:cursor-not-allowed disabled:opacity-50"
        >
          <Bolt width={15} height={15} />
          {t("refineSubmit")}
        </button>
      </div>
      <p className="mt-1.5 text-xs text-muted">{t("refineHint")}</p>
    </form>
  );
}

/** Transparency: collapsible view of the exact prompt sent to the model. */
export function PromptDisclosure({ prompt }: { prompt: string }) {
  const t = useT(T);
  const [open, setOpen] = useState(false);
  return (
    <div className="card overflow-hidden">
      <div className="flex w-full items-center justify-between px-5 py-4 text-sm font-medium text-navy-800">
        <button type="button" onClick={() => setOpen((v) => !v)} className="flex items-center gap-2">
          <Info width={16} height={16} className="text-brand-600" />
          {t("showPrompt")}
        </button>
        <span className="flex items-center gap-1.5">
          {/* The transparency panel is the selling point — let the prospect take the
              engineered prompt with one click instead of hand-selecting the <pre>. */}
          <CopyButton text={prompt} />
          <button type="button" onClick={() => setOpen((v) => !v)} className="text-muted">
            {open ? t("hidePrompt") : t("showPromptToggle")}
          </button>
        </span>
      </div>
      {open && (
        <pre className="overflow-x-auto border-t border-line bg-onyx px-5 py-4 font-mono text-xs leading-relaxed text-onyx-ink">
          {prompt}
        </pre>
      )}
    </div>
  );
}

