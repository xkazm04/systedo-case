/** Error state with a retry handler. When the hook surfaces the typed AiError
 *  envelope, a rate-limit renders as a live countdown that re-enables the retry
 *  button at zero, and a quota error renders the server-provided upgrade link —
 *  a self-healing wait instead of a dead-end string. Buttons ride the shared
 *  design-system primitive (Button / buttonClass). */
export function ToolError({
  message,
  onRetry,
  retryIn,
  upgradeUrl,
}: {
  message: string;
  onRetry: () => void;
  /** seconds until retry is allowed (rate limit) — counts down to 0 in the hook */
  retryIn?: number | null;
  /** upgrade path for a spent quota (e.g. /cena) */
  upgradeUrl?: string | null;
}) {
  const t = useT(T);
  const waiting = typeof retryIn === "number" && retryIn > 0;
  return (
    <div className="card animate-fade-in border-negative/30 p-6">
      <p className="text-sm font-semibold text-negative">{t("generationFailed")}</p>
      <p className="mt-1 text-sm text-muted">{message}</p>
      <div className="mt-4 flex flex-wrap items-center gap-2">
        <Button variant="secondary" size="sm" onClick={onRetry} disabled={waiting}>
          {waiting ? t("retryInBtn", { n: retryIn }) : t("retry")}
        </Button>
        {upgradeUrl && (
          <Link href={upgradeUrl} className={buttonClass("primary", "sm")}>
            {t("upgradeCta")}
          </Link>
        )}
      </div>
    </div>
  );
}

/** Animated loading indicator: a ring that fills toward the ~15s target the
 *  model usually needs. Results render the instant the response arrives (the
 *  ring is purely visual); past the target it keeps spinning until the hard limit. */
export function LoadingTimer() {
  const t = useT(T);
  const [elapsed, setElapsed] = useState(0); // seconds

  useEffect(() => {
    const start = performance.now();
    const id = setInterval(() => setElapsed((performance.now() - start) / 1000), 100);
    return () => clearInterval(id);
  }, []);

  const target = AI_TIMER_TARGET_MS / 1000;
  const progress = Math.min(elapsed / target, 1);
  const over = elapsed >= target;
  const R = 42;
  const C = 2 * Math.PI * R;
  const color = over ? "var(--color-coral-500)" : "var(--color-brand-500)";

  return (
    <div
      data-testid="ai-loading"
      className="card flex animate-fade-in flex-col items-center justify-center p-10 text-center"
    >
      <div className="relative h-28 w-28">
        <svg className="h-full w-full -rotate-90" viewBox="0 0 100 100">
          <circle cx="50" cy="50" r={R} fill="none" stroke="var(--color-line)" strokeWidth="6" />
          <circle
            cx="50"
            cy="50"
            r={R}
            fill="none"
            stroke={color}
            strokeWidth="6"
            strokeLinecap="round"
            strokeDasharray={C}
            strokeDashoffset={C * (1 - progress)}
            className={over ? "animate-pulse" : ""}
            style={{ transition: "stroke-dashoffset 0.12s linear, stroke 0.3s ease" }}
          />
        </svg>
        <div className="absolute inset-0 grid place-items-center">
          <span className="tnum text-2xl font-semibold text-navy-800">{Math.floor(elapsed)}s</span>
        </div>
      </div>
      <p className="mt-5 text-base font-semibold text-navy-800">
        {over ? t("generatingLong") : t("generating")}
      </p>
      <p className="mt-1 max-w-xs text-sm text-muted">
        {t("resultAppears")}{over ? t("resultAppearsLimit", { n: AI_TIMEOUT_SECONDS }) : ""}
      </p>
    </div>
  );
}

