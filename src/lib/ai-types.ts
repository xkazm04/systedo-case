export type AiMode =
  | "ads"
  | "brief"
  | "analysis"
  | "lead-reply"
  | "repurpose"
  | "local-review-reply"
  | "article-draft"
  | "cohort-diagnosis"
  | "keyword-clusters"
  | "comparison-outline"
  | "lp-variant-ideas";
export const AI_MODES: AiMode[] = [
  "ads",
  "brief",
  "analysis",
  "lead-reply",
  "repurpose",
  "local-review-reply",
  "article-draft",
  "cohort-diagnosis",
  "keyword-clusters",
  "comparison-outline",
  "lp-variant-ideas",
];

export interface AiMeta {
  model: string;
  /** true when generated by the deterministic fallback (no provider available:
   *  no Claude CLI in dev / no GEMINI_API_KEY in prod) */
  demo: boolean;
  /** the exact prompt sent to the model — surfaced in the UI for transparency */
  prompt: string;
  tookMs: number;
  /** model tag of the provider that actually served the result */
  provider?: string;
  /** total provider attempts, including retries and any self-repair re-prompt */
  attempts?: number;
  /** true when the environment-preferred provider failed and another provider
   *  (or the deterministic demo) served the result instead */
  fellBack?: boolean;
  /** true when the output was re-prompted once to correct limit violations */
  repaired?: boolean;
  /** limit violations detected in the first model output (before repair/clamp) */
  violations?: string[];
  /** token usage when the provider reports it (Gemini); absent for the Claude
   *  subscription path */
  usage?: { inputTokens: number; outputTokens: number; totalTokens: number };
  /** estimated USD cost of the call; 0 for the dev Claude subscription path */
  estCostUsd?: number;
}

export interface AiResponse<T> {
  result: T;
  meta: AiMeta;
}

