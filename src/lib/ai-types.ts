// ===========================================================================
// Tool 9 — cohort diagnosis (CAC → LTV: an AI read of the computed cohort
// economics — names the problem cohort and the single lever to pull first.
// Grounded strictly in the numbers the module already computed; the model
// invents no figures.)
// ===========================================================================

/** One cohort's economics, flattened to the few numbers the diagnosis needs.
 *  A serializable projection of CohortMetrics (lib/ltv/compute) so the client
 *  never ships the full curve/channel graph to the model. */
export interface CohortDiagnosisCohort {
  /** the cohort month label (e.g. „2025-01") — also the worstCohort identifier */
  month: string;
  /** acquisition cost per signup (CZK) */
  cac: number;
  /** lifetime value per user (CZK) */
  ltv: number;
  /** LTV : CAC ratio */
  ltvCac: number;
  /** 1-based payback month, or null when not recovered within the horizon */
  paybackMonth: number | null;
  /** M3 retention as a fraction (0–1) */
  m3: number;
  /** signups in the cohort */
  signups: number;
}

/** Blended portfolio summary + optional trend direction handed to the model. */
export interface CohortDiagnosisRequest {
  /** the per-cohort rows, chronological (oldest first) — REAL numbers only */
  cohorts: CohortDiagnosisCohort[];
  /** blended CAC across all cohorts (CZK) */
  blendedCac: number;
  /** average LTV : CAC across cohorts */
  avgLtvCac: number;
  /** average payback month across cohorts, or null */
  avgPayback: number | null;
  /** the cohort trend direction, when there are ≥ 2 cohorts */
  trend?: TrendDirection;
}

/** The trend direction shape, mirrored from lib/ltv/compute so the client+server
 *  contract stays free of server (compute) imports. */
export type TrendDirection = "improving" | "worsening" | "flat";

export interface CohortDiagnosisResult {
  /** one short paragraph reading the unit economics */
  summary: string;
  /** the month label of the weakest cohort (must be one of the supplied labels) */
  worstCohort: string;
  /** the single most impactful lever to pull first, as a concrete action */
  recommendation: string;
  /** optional 1–3 risks to watch */
  risks?: string[];
}

export type CohortDiagnosisResponse = AiResponse<CohortDiagnosisResult>;

// ===========================================================================
// Tool 10 — keyword intent-clustering (Klíčová slova: regroup a flat keyword
// list into topic clusters, each a pillar keyword + supporting keywords —
// ready-made content structure. Generic, so the content-engine module can reuse
// it. Grounded strictly in the supplied keywords; the model invents none.)
// ===========================================================================

/** One supplied keyword fed into clustering — keyword plus optional real metrics
 *  carried over from the keyword-research tool (volume / classified intent). */
export interface KeywordClusterInput {
  /** the keyword phrase (the only required field) */
  keyword: string;
  /** average monthly searches, when known — drives pillar pick + totalVolume */
  volume?: number;
  /** the search intent, when classified (informational | transactional | brand) */
  intent?: string;
}

export interface KeywordClustersRequest {
  /** the keywords to regroup — REAL input set; the model must not invent more */
  keywords: KeywordClusterInput[];
  /** optional overarching topic, to steer the grouping */
  topic?: string;
}

/** One topic cluster: a single pillar keyword + its supporting keywords, with the
 *  summed monthly volume of every keyword in the cluster. */
export interface KeywordCluster {
  /** short topic name for the cluster */
  topic: string;
  /** the dominant search intent, when the model assigned one */
  intent?: KeywordIntent;
  /** the one pillar keyword (the page to build first) — always from the input */
  pillar: string;
  /** the supporting keywords (the subpages) — all from the input set */
  supporting: string[];
  /** summed monthly searches across pillar + supporting (from supplied volumes) */
  totalVolume?: number;
}

export interface KeywordClustersResult {
  /** the topic clusters, largest (by total volume) first */
  clusters: KeywordCluster[];
}

export type KeywordClustersResponse = AiResponse<KeywordClustersResult>;

